public class Printftype {
    public static void main(String[] args){

        int i = 3;
        double d = 3.0;
        String s = "문자열";


        /*printf() 메소드의 출력형식
        %d = 정수
        %f = 실수
        %s = 문자열
        */
        System.out.printf("%d",i);
        System.out.printf("%f",d);
        System.out.printf("%s",s);
    }
}
